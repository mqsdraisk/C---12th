// write a program to print text using the following escape sequences:

#include <iostream>
using namespace std;

int main(){

    // alert (\a):
    cout<<"This is an \a alert."<<endl;

    // backspace (\b):
    cout<<"This is backspace\b."<<endl;

    //newline (\n):
    cout<<"This is a\nnew line."<<endl;

    // carriage return:
    cout<<"This is \r carriage return"<<endl;

    // tab (\t):
    cout<<"This is \t tab."<<endl;

    // display backslash:
    cout<<"This is a \\ backslash"<<endl;

    // display quotations:
    cout<<"These are \"Quotation Marks\""<<endl;
}